package assignment1;

public class Vowels {
	public static void main(String[] args)
	{
		char cha = 'a';

	    if (cha == 'a' || cha == 'e' || cha == 'i' || cha == 'o' || cha == 'u' ||
	        cha == 'A' || cha == 'E' || cha == 'I' || cha == 'O' || cha == 'U') 
		{
	        System.out.printf("It is a vowel.");
	    } 
		else 
		{
	        System.out.printf("It is a consonant.");
	    }
	}

}
